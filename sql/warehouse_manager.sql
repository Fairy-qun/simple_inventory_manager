-- ----------------------------
-- Function structure for RECOVER_DATA
-- ----------------------------
CREATE OR REPLACE
PROCEDURE "ZOTION"."RECOVER_DATA" AS
BEGIN
	FOR rec IN (
			select start_quantity,inbound_quantity,outbound_quantity from daily_report
	) LOOP
			update daily_report set start_quantity = rec.start_quantity,
			endbound_quantity = rec.start_quantity + rec.inbound_quantity - rec.outbound_quantity; 
		END LOOP;
		
		commit;

END;
