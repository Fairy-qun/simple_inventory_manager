-- ----------------------------
-- Function structure for GENERATE_DAILY_REPORT
-- ----------------------------
CREATE OR REPLACE
PROCEDURE "ZOTION"."GENERATE_DAILY_REPORT" AS
BEGIN
		-- 插入数据到日报表中
	insert into daily_report(warehouse_code,material_code,report_date,start_quantity)
	select a.warehouse_code,b.material_code,a.inbound_time,sum(b.quantity)
	from inbound_order a
	join inbound_detail b
	on a.inbound_order_code = b.inbound_order_code
	group by a.warehouse_code,b.material_code,a.inbound_time;
	
	insert into daily_report(warehouse_code,material_code,report_date,inbound_quantity)
	select a.warehouse_code,b.material_code,a.inbound_time,b.quantity
	from inbound_order a
	join inbound_detail b on a.inbound_order_code = b.inbound_order_code;
	
	for rec in (
		select a.material_code,b.outbound_time as outbound_time,sum(a.quantity) as outbound_quantity 
		from outbound_detail a
		join outbound_order b on a.outbound_order_code = b.outbound_order_code
		group by a.material_code,b.outbound_time
	)
	LOOP
	update daily_report set outbound_quantity = rec.outbound_quantity where material_code = rec.material_code and report_date = rec.outbound_time;
    END LOOP;

	update daily_report set endbound_quantity = NVL(start_quantity,0) + NVL(inbound_quantity, 0) - NVL(outbound_quantity, 0);
	commit;
END;
/

-- ----------------------------
-- Function structure for GET_RANDOM_NUMBER
-- ----------------------------
CREATE OR REPLACE
FUNCTION "ZOTION"."GET_RANDOM_NUMBER" AS
BEGIN
	-- 生成1到1000之间的随机数
	select TRUNC(DBMS_RANDOM.VALUE(1,1001)) INTO random_number from dual;
	
	RETURN random_number;
END;
